int main(int argc, char *argv[]) 
{
	// ´NSAutoreleasePool *pool = [[NSAutoreleasePool alloc] init];

	@autoreleasepool {

		// Prefix with : to suppress the default error messages from getopt_long, which we will handle ourselves
		const char *short_opts = ":hd:s:";

		// Dithering options/algorithms/patterns (-d | --dither): bayer, void, force, magic
		// Matrix size (-s | --size): 2, 3, 4, 8, 16
		static struct option long_opts[] = {
			{"help", no_argument, NULL, 'h'},
			{"dither", required_argument, NULL, 'd'},
			{"size", required_argument, NULL, 's'},
			{NULL, 0, NULL, 0}
		};
	
		// https://www.cs.wm.edu/~smherwig/courses/csci415-f2024/topics/warmup/getopt/index.html

	

	// Specify the path to the image
	if (argc < 2) {
		// TODO: Create a function to print the usage
		// Look at some utility on my work Mac on how to print a good usage help message
		//printf("usage: %s path/to/image [dither_level]\n", argv[0]);
		print_usage(argv);
		exit(EXIT_FAILURE);
	}
	
	// Get the specified image path
	// Fix this, it will be the the next option after the optind
	// NSString *imagePath = [NSString stringWithUTF8String:argv[1]];
	int ditherLevel = 16;
	DitherType ditherType = BAYER; // Default the Bayer matrix type 

	int ch;
	NSString *ditherArg   = nil;
	NSString *inputFile   = nil;

	// 1. Parse the flagged options
	while ((ch = getopt_long(argc, argv, short_opts, long_opts, NULL)) != -1) {
		switch (ch) {
			case 'd':  // Dither matrix type
				ditherArg = [[NSString stringWithUTF8String:optarg] lowercaseString];
				break;
			case 's': // Dither size
				ditherLevel = atoi(optarg);
				break; 
			case 'h':
			default:
				printf("Usage: program_name --dither <val> --size <val> <input_file>\n");
				return 0;
		}
	}

	NSLog(@"ditherArg: %@, ditherLevel: %d", ditherArg, ditherLevel);
	NSLog(@"optind: %d | argc: %d", optind, argc);

	// 2. Parse out the dither matrix type
	if (ditherArg != nil) {
		if ([ditherArg isEqualToString: @"bayer"] == YES) {
			ditherType = BAYER;
		} else if ([ditherArg isEqualToString: @"force"] == YES) {
			ditherType = FORCE;
		} else if ([ditherArg isEqualToString: @"magic"] == YES) {
			ditherType = MAGIC;
		} else if ([ditherArg isEqualToString: @"shidoku"] == YES) {
			ditherType = SHIDOKU;
			ditherLevel = 4; // Shidoku is always 4x4
		} else if ([ditherArg isEqualToString: @"void"] == YES) {
			ditherType = VOID;
		}
	}

	// 3. Grab the last positional argument
	// optind is the index of the first non-option argument
	if (optind < argc) {
		// We take the very last argument as the input file
		inputFile = [NSString stringWithUTF8String:argv[argc - 1]];
	} else {
		// TODO: Print usage since there is no input file
		print_usage(argv);
		return EXIT_FAILURE;
	}

	// Import into an NSData object or NSImage
	NSLog(@"inputFile: %@", inputFile);
	NSImage *originalImage = [[NSImage alloc] initWithContentsOfFile: inputFile];
	
	if (originalImage == NULL)
	{
		NSLog(@"The image failed to load");
		return EXIT_FAILURE;
	}
	
	NSSize originalImageSize = [originalImage size];
	double aspectRatio = 1.0;
	CGFloat newImageHeight = originalImageSize.height;
	CGFloat newImageWidth = originalImageSize.width;
	NSBitmapImageRep *bitmap = [[originalImage representations] objectAtIndex: 0];
	int col    = 0;
	int row    = 0;
	int modVal = ditherLevel;
	dispatch_queue_t queue = dispatch_get_global_queue(DISPATCH_QUEUE_PRIORITY_DEFAULT, 0);
	size_t originalHeight = (int)originalImageSize.height;
	size_t originalWidth  = (int)originalImageSize.width;

	// Cycle through each pixel and find the nearest color and replace it in the standard EGA palette
	for (size_t y = 0; y < originalHeight; y++)
	// dispatch_apply(originalHeight, queue, ^(size_t y) {
	{
		row = y % modVal; // y & 15 == y % 16

		for (size_t x = 0; x < originalWidth; x++)
		{
			col = x % modVal; // x & 15 == x % 16

			// NSColor *originalPixelColor = [bitmap colorAtX:x y:y]; //  [[bitmap colorAtX:x y:y] colorUsingColorSpace:[NSColorSpace sRGBColorSpace]]; // [originalColor colorUsingColorSpace:[NSColorSpace sRGBColorSpace]];
			NSColor *originalPixelColor = [[bitmap colorAtX:x y:y] colorUsingColorSpace:[NSColorSpace sRGBColorSpace]];
			CGFloat red   = [originalPixelColor redComponent];
			CGFloat green = [originalPixelColor greenComponent];
			CGFloat blue  = [originalPixelColor blueComponent];
			CGFloat alpha = [originalPixelColor alphaComponent];

			// Divide by 255.0 to normalize the value
			CGFloat bayerDitherValue =  ditherValue(ditherType, ditherLevel, col, row);

			// https://brandonrohrer.com/convert_rgb_to_grayscale.html
			// The Standard Weighted Formula

			// To convert a color image to grayscale or to calculate "perceived" error, we use a weighted average:

			// Y=0.299R+0.587G+0.114B
			// Green (58.7%): Carries the most detail and "brightness" information.

			// Red (29.9%): Carries mid-level brightness.

			// Blue (11.4%): Contributes very little to perceived lightness; we use it mostly for color "tint."
			CGFloat lumaGrayValue = (0.299 * red) + (0.587 * green) + (0.114 * blue);

			// CGFloat avgPixelColor = (red + green + blue)/3.0; // TODO: Can use other methods to attribute to gamma correction // (0.299, 0.587, 0.114));
			CGFloat newColor = 0.0;

			// For some of the matrices (8x8 and 16x16), if the avgPixelColor is full black or white might be 
			// incorrectly calculated.
			if (lumaGrayValue == 0.0 || lumaGrayValue == 1.0) {
				newColor = lumaGrayValue;
			} else {
				newColor = lumaGrayValue < bayerDitherValue ? 0 : 1.0;
			}

			NSColor *newPixelColor = [NSColor colorWithCalibratedRed: newColor green: newColor blue: newColor alpha: 1.0];
			// Trying this next line creates errors: "Unrecognized colorspace number -1" and "Unknown number of components for colorspace model -1"
			// NSColor *newPixelColor = [NSColor colorWithWhite: newColor alpha: alpha];
			[bitmap setColor: newPixelColor atX: x y: y];
		}
	}

	// Convert bitmap back into an NSImage
	NSImage *oneBitDitheredImage = [[NSImage alloc] initWithSize:[bitmap size]];
	[oneBitDitheredImage addRepresentation: bitmap];

	// Save NSBitmapImageRep to an image and save to disk
	NSString *fileName = [inputFile stringByDeletingPathExtension];
	NSString *oneBitDitheredImagePath = [NSString stringWithFormat:@"%@_%@_dithered_%dx%d.png", fileName, ditherArg, modVal, modVal];
	NSBitmapImageRep *imgRep = [[oneBitDitheredImage representations] objectAtIndex: 0];
	NSData *data = [imgRep representationUsingType: NSPNGFileType properties: nil];
	[data writeToFile: oneBitDitheredImagePath atomically: NO];

	// [pool release];
	}
	return 0;
}

CGFloat ditherValue(DitherType ditherType, int ditherLevel, int col, int row) {

	// printf("Dither type: %d | Dither level: %d ", ditherType, ditherLevel);
	
	switch (ditherType) {
		case FORCE:
			if (ditherLevel == 8) {
				return forced_field8x8[col][row] / 63.0; 
			} else {
				return forced_field16x16[col][row] / 255.0;
			}
			break;
		case MAGIC:
			if (ditherLevel == 3) {
				return magic_square3x3[col][row] / 9.0;
			} else if (ditherLevel == 4) {
				return magic_square4x4_alt[col][row] / 15.0;
				// return magic_square4x4[col][row] / 16.0;
			} else {
				return magic_square5x5[col][row] / 25.0;
			}
			break;
		case SHIDOKU:
			return shidoku_matrix4x4[col][row] / 4.0;
			break;
		case VOID:
			if (ditherLevel == 8) {
				return void_cluster8x8[col][row] / 63.0;
			} else if (ditherLevel == 16) {
				return void_cluster16x16[col][row] / 255.0;
			} else if (ditherLevel == 32) {
				return void_cluster32x32[col][row] / 1023.0;
			} else {
				return void_cluster64x64[col][row] / 4095.0; 
				}
			break;
		case BAYER:
		default: // For Bayer and the default case
			if (ditherLevel == 2) {
				return BAYER_PATTERN_2X2[col][row] / 255.0;
			} else if (ditherLevel == 3) {
				return BAYER_PATTERN_3X3[col][row] / 255.0;
